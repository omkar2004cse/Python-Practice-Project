# assignment operator =,+=,-=,*=,/=,%=,**=,//=
num1=12

num1 += 4
print("num1 += 4:-",num1) # num1 = num1 + 4  =>(num1 += 4)

num1 -= 7
print("num1 -= 7:-",num1) # num1 = num1 - 7  =>(num1 -= 7)

num1 *= 2
print("num1 *= 2:-",num1) # num1 = num1 * 2  =>(num1 *= 2)

num1 /= 5
print("num1 /= 5:-",num1) # num1 = num1 / 5  => (num1 /= 5)
 
num1 %= 2
print("num1 %= 2:-",num1) # num1 = num1 % 2  => (num1 %= 2)

num1 **= 3
print("num1 **= 3:-",num1) # num1 = num1 ** 3  => (num1 **= 3) 

num1 //= 3
print("num1 //= 3:-",num1) # num1 = num1 // 3  => (num1 //= 3)

 